CREATE PROCEDURE INSERT_ANIMALS

    @Description NVARCHAR(100),
    @User NVARCHAR(50),
    @Enable BIT,
    @Name NVARCHAR(50),
    @Type NVARCHAR(50),
    @OldDate NVARCHAR(50)

AS

BEGIN

    INSERT INTO [ANIMALS] 
    ([ANIMALS_DESCRIPTION], [ANIMALS_USER], [ANIMALS_ENABLE], [ANIMALS_NAME], [ANIMALS_TYPE], [ANIMALS_OLDDATE])
    VALUES 
    (@Description, @User, @Enable, @Name, @Type, @OldDate);
    
END